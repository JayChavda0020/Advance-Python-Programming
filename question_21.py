# What are oops concepts? Is multiple inheritance supported in java

''' oops concepts help developers structure their code in a way that models real-world entities and promotes code reusability, maintainability, and modularity. The main OOP concepts include:

1) Classes and Objects:
Class: A blueprint for creating objects. It defines attributes (data) and methods (functions) that objects of that class will have.
Object: An instance of a class. It represents a specific entity with its own state (attributes) and behavior (methods).

2) Encapsulation:
Encapsulation refers to bundling the data (attributes) and methods (functions) that operate on the data into a single unit (class).
It hides the internal state of an object and only exposes the necessary functionality through methods.

3) Abstraction:
Abstraction involves hiding the complex implementation details and exposing only the essential features of an object. 
It allows developers to work at a higher level of abstraction without worrying about the low-level details.

4) Inheritance:
Inheritance is a mechanism where a new class (subclass or derived class) can inherit properties (attributes and methods) from an existing 
class (superclass or base class). It promotes code reuse and supports the "is-a" relationship.

5) Polymorphism:
Polymorphism allows objects to be treated as instances of their superclass. It enables a single interface to represent multiple underlying 
data types. Polymorphism can be achieved through method overloading (having multiple methods with the same name but different parameters) 
and method overriding (subclass provides a specific implementation for a method defined in the superclass).'''

# No, Java does not support multiple inheritance 